<?php

namespace App;

use Doctrine\ORM\Mapping AS ORM;

class Owners
{
    protected $id;

    protected $name;

    protected $phone;


    public function getId()
    {
        return $this->id;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }

    public function getPhone()
    {
        return $this->phone;
    }

    public function setPhone($phone)
    {
        $this->phone = $phone;
    }
}